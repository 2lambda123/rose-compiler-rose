#ifndef ROSE_NELMTS_H
#define ROSE_NELMTS_H

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Number of elements in a statically allocated array.
//
//    int foo[5];
//    assert(ROSE_NELMTS(foo) == 5)
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#ifndef ROSE_NELMTS
#define ROSE_NELMTS(X) (sizeof(X) / sizeof((X)[0]))
#endif

#endif
